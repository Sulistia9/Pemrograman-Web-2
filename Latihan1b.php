//Pengurangan
$hasilkurang = $hasilbagi - 6;
echo "Jika aku di kurang 6, jumlahku sekarang $hasilkurang. </br>";

//Pertambahan
$hasiltambah = $hasilkurang + 2;
echo "Jika aku di tambah 2, jumlahku sekarang $hasiltambah. </br>";
