//Satuan
echo "Aku adalah angka $a. </br>";

//Perkalian
$hasilkali = $a * 8;
echo "Jika aku di kali 8, jumlahku sekarang $hasilkali. </br>";

//Pembagian
$hasilbagi = $hasilkali / 4;
